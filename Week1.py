# Python Dictionary

contact = {'Bill': 'Bill@gmail.com', 'Kevin': 'Kevin@gmail.com'}

for name in contact.keys():
    print(name)

for email in contact.values():
    print(email)

for name, email in contact.items():
    print(name, email)


x = ('Bill', 'Gates', 'gates@gmail.com')
fname, lname, email = x

print(fname, lname, email)

